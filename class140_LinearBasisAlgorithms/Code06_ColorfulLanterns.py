MAXN = 51
BIT = 50

# 线性基数组
basis = [0] * (BIT + 1)
# 灯泡数和开关数
n = 0
m = 0

def insert(num):
    """
    将数字插入线性基
    算法思路：
    1. 从高位到低位扫描
    2. 如果当前位为1且线性基中该位为空，则直接插入
    3. 否则用线性基中该位的数异或当前数，继续处理
    @param num: 要插入的数字
    @return: 如果成功插入返回True，否则返回False
    """
    for i in range(BIT, -1, -1):
        if (num >> i) & 1:
            if basis[i] == 0:
                basis[i] = num
                return True
            num ^= basis[i]
    return False

def compute():
    """
    计算彩灯可以展示的样式数目
    算法思路：
    1. 每个开关控制的灯泡状态可以用一个二进制数表示
    2. 所有可能的样式数目等于线性基能表示的不同异或值数目
    3. 如果线性基中有k个元素，则能表示2^k种不同的值
    4. 由于结果可能很大，需要对2008取模
    时间复杂度：O(M * N)
    空间复杂度：O(N)
    @return: 彩灯可以展示的样式数目对2008取模的结果
    """
    # 清空线性基
    for i in range(BIT + 1):
        basis[i] = 0
    
    # 读取每个开关控制的灯泡范围并构建线性基
    for i in range(1, m + 1):
        control = input().strip()
        mask = 0
        for j in range(n):
            if control[j] == 'O':
                mask |= (1 << j)
        insert(mask)
    
    # 计算线性基中非零元素的个数
    count = 0
    for i in range(BIT + 1):
        if basis[i] != 0:
            count += 1
    
    # 计算2^count % 2008
    result = 1
    for i in range(1, count + 1):
        result = (result * 2) % 2008
    
    return result

def main():
    """
    主函数
    读取输入数据，调用计算函数，输出结果
    """
    global n, m
    
    # 读取灯泡数和开关数
    line = input().split()
    n = int(line[0])
    m = int(line[1])
    
    # 计算并输出结果
    print(compute())

