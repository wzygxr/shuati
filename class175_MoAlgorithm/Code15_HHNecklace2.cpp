// 对于每个数字，我们只关心它是否在当前窗口中出现过
// 可以使用计数数组记录每个数字的出现次数，用一个变量记录不同数字的种类数

// 时间复杂度分析：
// 1. 预处理排序：O(m * log m)
// 2. 莫队算法处理：O((n + m) * sqrt(n))
// 3. 总时间复杂度：O(m * log m + (n + m) * sqrt(n))
// 空间复杂度分析：
// 1. 存储原数组：O(n)
// 2. 存储查询：O(m)
// 3. 计数数组：O(max(ai)) = O(10^6)
// 4. 总空间复杂度：O(n + m + 10^6)

